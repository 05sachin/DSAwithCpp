#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main(){
    int x=3;
    cout<<(x<<1)<<endl;//left shift operator x<<y mean x*2^y
    int y=4;
    cout<<(x<<y)<<endl;

    // right shift operator x>>y mean x/2^y
    int t=33;
    cout<<(t>>1)<<endl;
    cout<<(t>>y)<<endl;
    
}